    public AddScreenSeat(screenseatinfoTO cit) {
        this();
        if (cit != null) {
            jtfSeatName.setText(cit.getSeatname());
            for(int i = 1; i < jcbScreenName.getItemCount(); i++){
                screeninfoTO mit = (screeninfoTO)jcbScreenName.getItemAt(i);
                if(mit.getScreenID().equals(cit.getScreenID())){
                    jcbScreenName.setSelectedIndex(i);
                    break;
                }
            }
            jcbScreenName.setSelectedItem(cit.getScreenname());
            for(int i = 1; i < jcbSeatType.getItemCount(); i++){
                seattypeTO mit = (seattypeTO)jcbSeatType.getItemAt(i);
                if(mit.getTypeID()== cit.getTypeID()){
                    jcbSeatType.setSelectedIndex(i);
                    break;
                }
            }
            jcbSeatType.setSelectedItem(cit.getSeattype());
            setTitle("Update Screen Seat Information");
            btnScreenSeat.setText("Update");
        }
    }

