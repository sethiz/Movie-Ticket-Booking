    private void btnScreenSeatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnScreenSeatActionPerformed
        String message = "";
        boolean allvalid = true;
        String seat = jtfSeatName.getText().trim();
        if (Validations.isEmpty(seat)) {
            message += "Please Enter Any Seat Name\n\n";
            allvalid = false;
        }
        if (jcbScreenName.getSelectedIndex() == 0) {
            message += "Please Choose Any Screen\n\n";
            allvalid = false;
        }
        if (jcbSeatType.getSelectedIndex() == 0) {
            message += "Please Choose Any Seat";
            allvalid = false;
        }
        if (allvalid) {
            screeninfoTO cit = (screeninfoTO) jcbScreenName.getSelectedItem();
            seattypeTO sit = (seattypeTO) jcbSeatType.getSelectedItem();
            screenseatinfoTO ssi = new screenseatinfoTO();
            ssi.setSeatname(seat);
            ssi.setScreenID(cit.getScreenID());
            ssi.setTypeID(sit.getTypeID());
            screenseatinfoDAO action = new screenseatinfoDAO();
            if (btnScreenSeat.getText().contains("Add")) {
                if (action.insertRecord(ssi)) {
                    message = "This Screen is Assigned with Entered Seatname";
                    jcbScreenName.setSelectedIndex(0);
                    jtfSeatName.setText("");
                    JInternalFrame[] jif = getDesktopPane().getAllFrames();
                    if (jif != null) {
                        for (JInternalFrame jf : jif) {
                            if (jf instanceof ViewMovieTiming) {
                                ViewMovieTiming vci = (ViewMovieTiming) jf;
                                vci.bindTimingTable();
                                break;
                            }
                        }
                    }
                } else {
                    message = action.getErrormessgae();
                }
            } else if (btnScreenSeat.getText().contains("Update")) {
                if (action.updateRecord(ssi)) {
                    message = "Screen Seat Information is Updated in System";
                    jcbScreenName.setSelectedIndex(0);
                    jtfSeatName.setText("");
                } else {
                    message = action.getErrormessgae();
                }
                ViewScreenSeat vci = new ViewScreenSeat();
                vci.setVisible(true);
                getDesktopPane().add(vci);
                try {
                    vci.setSelected(true);
                } catch (PropertyVetoException ex) {
                }
                dispose();
            }
        }
        JOptionPane.showMessageDialog(this, message);
    }//GEN-LAST:event_btnScreenSeatActionPerformed

    private void jcbScreenNameItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbScreenNameItemStateChanged
        jcbSeatType.removeAllItems();
        jcbSeatType.addItem("Choose Seat Type");
        if (!(jcbScreenName.getSelectedIndex() == 0)) {
            List<seattypeTO> seats = new seattypeDAO().getAllRecord();
            if (seats != null && seats.size() > 0) {
                for (seattypeTO sit : seats) {
                    jcbSeatType.addItem(sit);
                }
            }
        }
    }//GEN-LAST:event_jcbScreenNameItemStateChanged


