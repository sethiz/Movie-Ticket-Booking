    private void editRecord() {
        if (row != -1 && screens != null && row < screens.size()) {
            screenseatinfoTO cit = screens.get(row);
            AddScreenSeat aci = new AddScreenSeat(cit);
            aci.setVisible(true);
            getDesktopPane().add(aci);
            try {
                aci.setSelected(true);
            } catch (PropertyVetoException ex) {

            }
            dispose();
        }
    }
    
    private void deleteRecord() {
        if (row != -1 && screens != null && row < screens.size()) {
            int result = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this record?", "Confirm Delete?", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if(result == JOptionPane.YES_OPTION){
                screenseatinfoTO cit = screens.get(row);
                if(cit!=null){
                    String message = "";
                    screenseatinfoDAO action = new screenseatinfoDAO();
                    if(action.deleteRecord(String.valueOf(cit.getSrNo()))){
                        message = "Screen Seat Information is Removed from System";
                        bindScreenSeatTable();
                    }else{
                        message = action.getErrormessgae();
                    }
                    JOptionPane.showMessageDialog(this, message);
                }
            }
        }
        row = -1;
    }

    public void bindScreenSeatTable() {
        screens = new screenseatinfoDAO().getAllRecord();
        screenseatinfoTO s = new screenseatinfoTO();
        String[] colnames = {"Seat Name", "Screen Name", "Seat Type"};
        Object[][] records = null;
        if (screens != null && screens.size() > 0) {
            records = new Object[screens.size()][colnames.length];
            int i = 0;
            for (screenseatinfoTO sit : screens) {
                records[i] = new Object[]{sit.getSeatname(), sit.getScreenname(), sit.getSeattype()};
                i++;
            }
        } else {
            records = new Object[1][colnames.length];
            records[0] = new Object[]{"No Records", "No Records", "No Records"};
        }
        DefaultTableModel dtm = new DefaultTableModel(records, colnames) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tableScreenSeatInfo.getTableHeader().setReorderingAllowed(false);
        tableScreenSeatInfo.setAutoCreateRowSorter(true);
        tableScreenSeatInfo.setModel(dtm);
    }

